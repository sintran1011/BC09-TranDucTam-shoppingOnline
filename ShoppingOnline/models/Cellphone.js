function Product(
  id,
  name,
  price,
  screen,
  backCamera,
  frontCamera,
  desc,
  type,
  img
) {
  this.id = id;
  this.name = name;
  this.price = price;
  this.screen = screen;
  this.backCamera = backCamera;
  this.frontCamera = frontCamera;
  this.desc = desc;
  this.type = type;
  this.img = img;
}
